  static Set<String>? _holidaySetCache;

  /// 全部节假日 yyyy-MM-dd 集合（懒构建）
  static Set<String> _holidaySet() {
    final cached = _holidaySetCache;
    if (cached != null) return cached;
    final set = <String>{};
    _holidaysByYear.forEach((year, mmdd) {
      for (final d in mmdd.split(' ')) {
        set.add('$year-$d');
      }
    });
    _holidaySetCache = set;
    return set;
  }

  static String formatDateValue(DateTime date) {
    final m = date.month.toString().padLeft(2, '0');
    final d = date.day.toString().padLeft(2, '0');
    return '${date.year}-$m-$d';
  }

  /// uni-app parseTradeDate: 先匹配 ^yyyy[-/]M[-/]D，失败再退化为 Date 解析
  static DateTime? parseTradeDate(String? value) {
    if (value == null || value.isEmpty) return null;
    final matched = RegExp(r'^(\d{4})[-/](\d{1,2})[-/](\d{1,2})').firstMatch(value);
    if (matched != null) {
      return DateTime(
        int.parse(matched.group(1)!),
        int.parse(matched.group(2)!),
        int.parse(matched.group(3)!),
      );
    }
    return DateTime.tryParse(value);
  }

  /// uni-app isFundTradingDay: 周末非交易日；工作日中的法定节假日非交易日。
  /// （调休工作日恰为周末，已被周末规则排除，与源码结果一致。）
  static bool isFundTradingDay(DateTime date) {
    // DateTime.weekday: 周一=1 ... 周六=6 周日=7
    if (date.weekday == DateTime.saturday || date.weekday == DateTime.sunday) {
      return false;
    }
    return !_holidaySet().contains(formatDateValue(date));
  }

  /// uni-app addFundTradingDays: 从交易日起向后累加 [days] 个交易日（上限 60 天），
  /// 凑不齐返回 null（源码返回 ''）。
  static DateTime? addFundTradingDays(String? value, int days) {
    final date = parseTradeDate(value);
    if (date == null) return null;
    var added = 0;
    var cursor = date;
    for (var i = 0; i < 60 && added < days; i++) {
      cursor = cursor.add(const Duration(days: 1));
      if (isFundTradingDay(cursor)) added++;
    }
    return added == days ? cursor : null;
  }

  /// uni-app getTradeTimePeriod: 从时间串里取 HH:mm，< 15:00 → 「15:00前」否则「15:00后」
  static String tradeTimePeriod(String? value) {
    if (value == null || value.isEmpty) return '';
    final matched = RegExp(r'T?(\d{1,2}):(\d{2})').firstMatch(value);
    if (matched == null) return '';
    final minutes = int.parse(matched.group(1)!) * 60 + int.parse(matched.group(2)!);
    return minutes < 15 * 60 ? '15:00前' : '15:00后';
  }

  /// uni-app getTradeEffectiveText: 「收益生效时间 yyyy-MM-dd」
  /// 15:00 前 +1 个交易日，15:00 后 +2 个交易日
  static String tradeEffectiveText(String? value) {
    if (value == null || value.isEmpty) return '';
    final matched = RegExp(r'T?(\d{1,2}):(\d{2})').firstMatch(value);
    if (matched == null) return '';
    final minutes = int.parse(matched.group(1)!) * 60 + int.parse(matched.group(2)!);
    final effective = addFundTradingDays(value, minutes < 15 * 60 ? 1 : 2);
    return effective != null ? '收益生效时间 ${formatDateValue(effective)}' : '';
  }
