  '2026-10-01', '2026-10-02', '2026-10-05', '2026-10-06', '2026-10-07', // 国庆
};

String _two(int v) => v.toString().padLeft(2, '0');

/// 是否为闭市法定节假日（对齐 chinese-days 的 isHoliday && !isWorkday）
bool isCurveClosedHoliday(DateTime date) {
  return _kHolidayDates.contains('${date.year}-${_two(date.month)}-${_two(date.day)}');
}
