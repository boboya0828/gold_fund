/// 中国法定节假日表 — 对齐 uni-app curve.vue 使用的 chinese-days 包
/// (isHoliday && !isWorkday → 日历格标"休")
///
/// 由于日历只渲染周一~周五的格子（周末直接跳过），且调休上班日均在周末，
/// 这里只需收录"落在工作日"的法定节假日。
/// 覆盖 2024–2026（日历年选择范围为当前年-5 ~ 当前年+1，超出范围的年份不显示"休"标记）。
library;

const Set<String> _kHolidayDates = {
  // 2024
  '2024-01-01', // 元旦
  '2024-02-12', '2024-02-13', '2024-02-14', '2024-02-15', '2024-02-16', // 春节
  '2024-04-04', '2024-04-05', // 清明
  '2024-05-01', '2024-05-02', '2024-05-03', // 劳动节
  '2024-06-10', // 端午
  '2024-09-17', // 中秋
  '2024-10-01', '2024-10-02', '2024-10-03', '2024-10-04', // 国庆
  // 2025
  '2025-01-01', // 元旦
  '2025-01-28', '2025-01-29', '2025-01-30', '2025-01-31', // 春节
  '2025-02-03', '2025-02-04',
  '2025-04-04', // 清明
  '2025-05-01', '2025-05-02', '2025-05-05', // 劳动节
  '2025-06-02', // 端午
  '2025-10-01', '2025-10-02', '2025-10-03', // 国庆+中秋
  '2025-10-06', '2025-10-07', '2025-10-08',
  // 2026（国务院办公厅关于2026年部分节假日安排的通知）
  '2026-01-01', '2026-01-02', // 元旦
  '2026-02-16', '2026-02-17', '2026-02-18', '2026-02-19', '2026-02-20', // 春节
